# > OPERADOREs

"""
- Soma: +
- Subtração: -
- Multiplicação: *
- Divisão: /
- Divisão Inteira: //
- Resto da divisão: %
- Potência: **
"""

num1 = 10
num2 = 20

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 /  num2)
print(num1 // num2)
print(26 % 3)
print(2 ** 3)

